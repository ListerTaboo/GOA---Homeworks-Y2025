# 2) Control Flow
# Control flow ნიშნავს პროგრამის შესრულების მიმდევრობას.
# ის განსაზღვრავს, როგორ და რა რიგით შესრულდება ბრძანებები.
# ძირითადი control flow ტექნიკებია:
# 1. Sequence – ბრძანებები სრულდება ზედიზედ.
# 2. Selection (if, elif, else) – პირობების მიხედვით არჩევა.
# 3. Iteration (for, while) – ბრძანებების განმეორება ციკლის მეშვეობით.


# 3) ალგორითმი
# ალგორითმი არის ნაბიჯ-ნაბიჯ ინსტრუქცია ამოცანის გადასაჭრელად.
# ალგორითმის წარმოდგენის გზები:
# 1. ტექსტური აღწერა – ბუნებრივი ენით ახსნა.
# 2. ბლოკ-სქემა (flowchart) – ვიზუალური ფორმა.
# 3. ფსევდოკოდი – პროგრამის მსგავსად აღწერილი ნაბიჯები.
# მე უპირატესობას ვანიჭებ ბლოკ-სქემას, რადგან ვიზუალურად უფრო მარტივია ალგორითმის გაგება.


# 4) Flowchart
# ავიღოთ მაგალითი: for ციკლით რიცხვების დაბეჭდვა 0-იდან 5-მდე
# Flowchart:
# Start → i = 0 → i <= 5 ? → print(i) → i = i + 1 → უკან შედის პირობაში → თუ False → End


# 5) for ციკლი – რიცხვები 30-ის ჩათვლით
for i in range(31):
    print(i)

# 6) for ციკლი – 15-იდან 80-მდე
for i in range(15, 81):
    print(i)

# 7) for ციკლი – 0-იდან 88-ის ჩათვლით
for i in range(89):
    print(i)

# 8) for ციკლი – ყოველი მესამე რიცხვი 0-იდან 50-მდე
for i in range(0, 51, 3):
    print(i)

# 9) მომხმარებლის ორი რიცხვი
a = int(input("შეიყვანე პირველი რიცხვი: "))
b = int(input("შეიყვანე მეორე რიცხვი: "))
for i in range(a, b + 1):
    print(i)

# 10) ყველა ლუწი რიცხვი 100-მდე
