# ცელსიუსის ტემპერატურა
Celsius = 25  # შეგიძლიათ სხვა მთელ რიცხვადაც ჩასვათ

# ფარენჰეიტში გადაყვანა
Fahrenheit = (Celsius * 9/5) + 32

# კელვინში გადაყვანა
Kelvin = Celsius + 273.15

# შედეგის დაბეჭდვა
print("ცელსიუსი:", Celsius)
print("ფარენჰეიტი:", Fahrenheit)
print("კელვინი:", Kelvin)



