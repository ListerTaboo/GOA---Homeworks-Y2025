# / - ჩვეულებრივი გაყოფა (მიღებული პასუხი ყოველთვის რიცხვია ათწილადი ფორმით)
print(7 / 2)  # პასუხი: 3.5
print(10 / 4) # პასუხი: 2.5

# // - მთელი რიცხვით გაყოფა (იხსნება მხოლოდ მთელი ნაწილი, რაც დარჩება წილმომდევნო ნაწილამდე)
print(7 // 2)  # პასუხი: 3 (არ ჩანს .5, მხოლოდ მთელი ნაწილი)
print(10 // 4) # პასუხი: 2

# % - ნაშთიანი გაყოფა (განაპირობებს იმ რიცხვს რაც რჩება გაყოფის შემდეგ)
print(7 % 2)  # პასუხი: 1 (2*3 = 6, ნაშთი 1)
print(10 % 4) # პასუხი: 2 (4*2 = 8, ნაშთი 2)



