#6) კომენტარებით დაწერეთ რით განსხვავდება integer და float მონაცემთა ტიპები

# Integer (მთელი რიცხვი) — ეს არის მონაცემთა ტიპი, რომელიც ინახავს მხოლოდ მთელ რიცხვებს
# მაგ.: 1, -5, 0, 100, 2025 და ა.შ.
age = 25           # integer ტიპის ცვლადი
year = 2025        # integer

# Float (მცურავი წერტილი) — ეს არის მონაცემთა ტიპი, რომელიც ინახავს ათწილად რიცხვებს (წერტილით)
# მაგ.: 3.14, -0.5, 100.0, 2.718 და ა.შ.
temperature = 22.5   # float ტიპის ცვლადი
pi = 3.14159         # float

# Integer და Float განსხვავება:
# 1) Integer არ შეიცავს ათწილად ნაწილს, ხოლო Float შეიცავს.
# 2) Float უფრო ზუსტია, მაგრამ მოითხოვს მეტ მეხსიერებას.
# 3) მათემატიკური ოპერაციებისას Integer + Float → ყოველთვის გამოაქვს Float შედეგი.

# მაგალითები:
result_1 = age + 5        # Integer + Integer = Integer
result_2 = temperature + 2  # Float + Integer = Float

# შედეგების დაბეჭდვა
print("result_1 =", result_1)  # შედეგი იქნება 30 (integer)
print("result_2 =", result_2)  # შედეგი იქნება 24.5 (float)
