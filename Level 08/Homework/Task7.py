#7) integer და float მონაცემთა ტიპის ცვლადებზე შეასრულეთ 20 მაგალითი ოპერატორებით: +, -, *, /, **, %

#integer ტიპის ცვლადები
a = 10
b = 3

#float ტიპის ცვლადები
x = 5.5
y = 2.0

# + ოპერატორი (მატება)
print("1) a + b =", a + b)         # 10 + 3 = 13
print("2) x + y =", x + y)         # 5.5 + 2.0 = 7.5
print("3) a + x =", a + x)         # int + float = 15.5
print("4) b + y =", b + y)         # 3 + 2.0 = 5.0

# - ოპერატორი (გამოკლება)
print("5) a - b =", a - b)         # 10 - 3 = 7
print("6) x - y =", x - y)         # 5.5 - 2.0 = 3.5
print("7) a - x =", a - x)         # 10 - 5.5 = 4.5
print("8) b - y =", b - y)         # 3 - 2.0 = 1.0

# * ოპერატორი (გამრავლება)
print("9) a * b =", a * b)         # 10 * 3 = 30
print("10) x * y =", x * y)        # 5.5 * 2.0 = 11.0
print("11) a * y =", a * y)        # 10 * 2.0 = 20.0
print("12) b * x =", b * x)        # 3 * 5.5 = 16.5

# / ოპერატორი (გაყოფა)
print("13) a / b =", a / b)        # 10 / 3 ≈ 3.3333
print("14) x / y =", x / y)        # 5.5 / 2.0 = 2.75
print("15) a / y =", a / y)        # 10 / 2.0 = 5.0
print("16) x / b =", x / b)        # 5.5 / 3 ≈ 1.8333

# ** ოპერატორი (ხარისხი)
print("17) a ** b =", a ** b)      # 10³ = 1000
print("18) x ** y =", x ** y)      # 5.5² = 30.25

# % ოპერატორი (ნაშთის პოვნა)
print("19) a % b =", a % b)        # 10 % 3 = 1
print("20) x % y =", x % y)        # 5.5 % 2.0 = 1.5
